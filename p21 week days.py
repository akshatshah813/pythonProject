x=int(input("Enter weekday number"))

if x==1:
    print("it's Monday")
elif x==2:
    print("it's Tuesday")
elif x==3:
    print("it's Wednesday")
elif x==4:
    print("it's Thursday")
elif x==5:
    print("it's Friday")
elif x==6:
    print("it's Saturday")
elif x==7:
    print("it's Sunday")
else:
    print("Enter a valid number!")
